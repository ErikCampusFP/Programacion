# Ejercicio 2. Cálculo de área de un rectángulo
# Pide la base y la altura de un rectángulo y muestra su área.
#  Usa variables y operadores aritméticos.
# --------------------------------------------------------------------------

# Calculo del area y solicitud del los datos necesarios para sacar la area
base = int(input("Introduce la longitud de la base: "))
altura = int(input("Introduce la longitud de la altura: "))
area = (base*altura)

# Se muestra el resultado del area
print(f"El Area del rectangulo seleccionado es {area}")
