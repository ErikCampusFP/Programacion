# Ejercicio 7. Contador ascendente
# Pide un número n y muestra los números del 1 al n usando un bucle for.
# --------------------------------------------------------------------------

# Solicita el numero que sera el maximo del bucle
numero = int(input("Introduce el numero maximo de la cuenta: "))

# Genera y muestra el bucle
for i in range(1, numero + 1):
    print(i)